export const assignmentStatusLabel = (locale: Locale, status: string) => {
  switch (status) {
    case 'pending':
    case 'assigned':
      return pickLocalizedText(locale, appCopy.staffTasks.pending)
    case 'accepted':
      return locale === 'th' ? 'ตอบรับแล้ว' : locale === 'zh' ? '已接受' : 'Accepted'
    case 'in_progress':
      return pickLocalizedText(locale, appCopy.staffTasks.inProgress)
    case 'completed':
      return pickLocalizedText(locale, appCopy.staffTasks.completed)
    case 'declined':
    case 'cancelled':
      return pickLocalizedText(locale, appCopy.staffTasks.cancelled)
    default:
      return status
  }
}

